#!/bin/bash
set -euo pipefail

ptb_backend_profile_apply() {
  local backend="${1:-}"
  if [ -z "${backend}" ]; then
    echo "[backend_profile] ERROR: missing backend" >&2
    return 2
  fi

  case "${backend}" in
    vllm)
      export INFERENCE_BENCH_BASELINE_CONTAINER_NAME="${INFERENCE_BENCH_BASELINE_CONTAINER_VLLM_NAME:-${INFERENCE_BENCH_BASELINE_CONTAINER_NAME}}"
      export INFERENCE_BENCH_VENV_HOST="${INFERENCE_BENCH_VENV_VLLM_HOST:-${INFERENCE_BENCH_VENV_HOST}}"
      export INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP="${INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP_VLLM:-1}"
      export PTB_DISABLE_IMPORT_SHIMS="${PTB_DISABLE_IMPORT_SHIMS_VLLM:-1}"
      export INFERENCE_BENCH_STUB_OUTLINES="${INFERENCE_BENCH_STUB_OUTLINES_VLLM:-0}"
      export INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS="${INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS_VLLM:-0}"
      ;;
    torch)
      export INFERENCE_BENCH_BASELINE_CONTAINER_NAME="${INFERENCE_BENCH_BASELINE_CONTAINER_TORCH_NAME:-${INFERENCE_BENCH_BASELINE_CONTAINER_NAME}}"
      export INFERENCE_BENCH_VENV_HOST="${INFERENCE_BENCH_VENV_TORCH_HOST:-${INFERENCE_BENCH_VENV_HOST}}"
      export INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP="${INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP_TORCH:-1}"
      export PTB_DISABLE_IMPORT_SHIMS="${PTB_DISABLE_IMPORT_SHIMS_TORCH:-1}"
      export INFERENCE_BENCH_STUB_OUTLINES="${INFERENCE_BENCH_STUB_OUTLINES_TORCH:-0}"
      export INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS="${INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS_TORCH:-1}"
      ;;
    sglang)
      export INFERENCE_BENCH_BASELINE_CONTAINER_NAME="${INFERENCE_BENCH_BASELINE_CONTAINER_SGLANG_NAME:-${INFERENCE_BENCH_BASELINE_CONTAINER_NAME}}"
      export INFERENCE_BENCH_VENV_HOST="${INFERENCE_BENCH_VENV_SGLANG_HOST:-${INFERENCE_BENCH_VENV_HOST}}"
      export INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP="${INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP_SGLANG:-1}"
      export PTB_DISABLE_IMPORT_SHIMS="${PTB_DISABLE_IMPORT_SHIMS_SGLANG:-1}"
      export INFERENCE_BENCH_STUB_OUTLINES="${INFERENCE_BENCH_STUB_OUTLINES_SGLANG:-0}"
      export INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS="${INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS_SGLANG:-1}"
      ;;
    tgi)
      export INFERENCE_BENCH_BASELINE_CONTAINER_NAME="${INFERENCE_BENCH_BASELINE_CONTAINER_TGI_NAME:-${INFERENCE_BENCH_BASELINE_CONTAINER_NAME}}"
      export INFERENCE_BENCH_VENV_HOST="${INFERENCE_BENCH_VENV_TGI_HOST:-${INFERENCE_BENCH_VENV_HOST}}"
      export INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP="${INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP_TGI:-1}"
      export PTB_DISABLE_IMPORT_SHIMS="${PTB_DISABLE_IMPORT_SHIMS_TGI:-1}"
      export INFERENCE_BENCH_STUB_OUTLINES="${INFERENCE_BENCH_STUB_OUTLINES_TGI:-0}"
      export INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS="${INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS_TGI:-1}"
      ;;
    *)
      echo "[backend_profile] WARN: unknown backend '${backend}', using shared defaults" >&2
      ;;
  esac
}

ptb_backend_profile_print() {
  local backend="${1:-}"
  echo "[backend_profile] backend=${backend}"
  echo "[backend_profile] container=${INFERENCE_BENCH_BASELINE_CONTAINER_NAME}"
  echo "[backend_profile] venv=${INFERENCE_BENCH_VENV_HOST}"
  echo "[backend_profile] max_model_len=${INFERENCE_BENCH_MAX_MODEL_LEN}"
  echo "[backend_profile] skip_bootstrap=${INFERENCE_BENCH_SKIP_VENV_BOOTSTRAP:-0} disable_import_shims=${PTB_DISABLE_IMPORT_SHIMS:-0} disable_runtime_shims=${INFERENCE_BENCH_DISABLE_RUNTIME_SHIMS:-0}"
}
